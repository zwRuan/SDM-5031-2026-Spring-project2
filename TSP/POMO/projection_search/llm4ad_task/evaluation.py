"""LLM4AD evaluation task for POMO coordinate projection search.

Wraps ``POMOFastEvaluator`` so the LLM4AD EoH framework can score
candidate projection functions generated by the LLM.
"""

from __future__ import annotations

import os
import sys
from typing import Any, Callable

# Ensure POMO modules are importable.
_POMO_DIR = os.path.join(os.path.dirname(__file__), "..", "..")
if _POMO_DIR not in sys.path:
    sys.path.insert(0, _POMO_DIR)
if os.path.join(_POMO_DIR, "..") not in sys.path:
    sys.path.insert(0, os.path.join(_POMO_DIR, ".."))

from llm4ad.base import Evaluation
from projection_search.llm4ad_task.template import template_program, task_description
from projection_search.pomo_fast_eval import POMOFastEvaluator

__all__ = ["POMOProjectionEvaluation"]

# ---- Global evaluator singleton (loaded once) ----
_evaluator: POMOFastEvaluator | None = None
_eval_kwargs: dict = {}


def _get_evaluator() -> POMOFastEvaluator:
    global _evaluator
    if _evaluator is None:
        ckpt = _eval_kwargs.get(
            "checkpoint_path",
            os.path.join(_POMO_DIR, "result", "best_ckpt_2", "checkpoint-best.pt"),
        )
        device = _eval_kwargs.get("device", "cuda:0")
        val_dir = _eval_kwargs.get("val_data_dir", None)
        num_fast = _eval_kwargs.get("num_fast_eval", 32)
        use_aug = _eval_kwargs.get("use_aug", False)
        _evaluator = POMOFastEvaluator(
            checkpoint_path=ckpt,
            device=device,
            val_data_dir=val_dir,
            num_fast_eval=num_fast,
            use_aug=use_aug,
        )
    return _evaluator


def evaluate_projection(callable_func: Callable) -> float:
    """Score a projection function using the POMO model.

    Returns:
        Negative avg_gap (so higher = better, as LLM4AD expects maximisation).
        A large negative value means a large gap (bad projection).
    """
    try:
        evaluator = _get_evaluator()
        gap = evaluator.evaluate(callable_func)
        if gap is None or (isinstance(gap, float) and not (gap >= 0)):
            return -100.0
        return -float(gap)
    except Exception:
        return -100.0


class POMOProjectionEvaluation(Evaluation):
    """Evaluator for POMO coordinate projection functions.

    Each candidate is a Python function ``normalize(coords: Tensor) -> Tensor``
    that preprocesses TSP coordinates before the POMO neural solver.
    """

    def __init__(
        self,
        timeout_seconds: int = 30,
        checkpoint_path: str | None = None,
        val_data_dir: str | None = None,
        num_fast_eval: int = 32,
        use_aug: bool = False,
        device: str = "cuda:0",
        **kwargs,
    ):
        global _eval_kwargs
        _eval_kwargs = {
            "checkpoint_path": checkpoint_path,
            "val_data_dir": val_data_dir,
            "num_fast_eval": num_fast_eval,
            "use_aug": use_aug,
            "device": device,
        }

        super().__init__(
            template_program=template_program,
            task_description=task_description,
            use_numba_accelerate=False,
            timeout_seconds=timeout_seconds,
        )

    def evaluate_program(
        self, program_str: str, callable_func: Callable, **kwargs
    ) -> Any | None:
        """Evaluate a generated projection function.

        Returns:
            Negative avg_gap (float), where values closer to 0 are better.
            Returns a very negative value (~ -100) on error.
        """
        return evaluate_projection(callable_func)
